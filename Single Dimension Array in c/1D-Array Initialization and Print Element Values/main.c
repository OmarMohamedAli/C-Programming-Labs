#include <stdio.h>
#include <stdlib.h>

int main()
{
  // 1D-Array Declaration
    char A[4];

  // 1D-Array Initialization
    A[0]=0;
    A[1]=2;
    A[2]=4;
    A[3]=6;

  // Print 1D-Array
    printf("First Element in 1D Array is : %d \n",A[0]);
    printf("Second Element in 1D Array is: %d \n",A[1]);
    printf("Third Element in 1D Array is : %d \n",A[2]);
    printf("Fourth Element in 1D Array is: %d \n",A[3]);

    return 0;
}
